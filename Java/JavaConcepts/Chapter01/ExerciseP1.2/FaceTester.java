/*
Chapter 1 - Exercise P1.2

Write a program that prints a face, using text characters, hopefully better looking than this one:
 ///////
 | o o |
(|	^  |)
 | [_] |
 -------	
Use comments to indicate the statements that print the hair, ears, mouth, and so on.
*/

public class FaceTester
{
	public static void main(String[] args)
	{
		//print out hair
		System.out.println(" /////// ");
		//print our eyes
		System.out.println(" | o o | ");
		//print out nose and ears
		System.out.println("(|	^  |)");
		//print out nose
		System.out.println(" | [_] | ");
		//print out chin
		System.out.println(" ------- ");
	}
}